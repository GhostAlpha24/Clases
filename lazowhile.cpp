#include <iostream>
using namespace std;

int main() {
    int i = 1; // Inicializamos la variable i con 1

    // Mientras i sea menor o igual a 9, se ejecutará el bucle
    while (i <= 9) {
        cout << i << " "; // Imprimimos el valor de i seguido de un espacio en blanco
        i++; // Incrementamos i en 1 en cada iteración
    }

    cout << endl; // Imprimimos un salto de línea al final
    return 0; // Indicamos que el programa finalizó correctamente
}
